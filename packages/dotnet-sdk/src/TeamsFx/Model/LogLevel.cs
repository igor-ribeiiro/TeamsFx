namespace Microsoft.TeamsFx.Model
{
    /// <summary>
    /// Log Level
    /// </summary>
    public enum LogLevel
    {
        /// <summary>
        /// Show verbose, information, warning and error message.
        /// </summary>
        Verbose = 0,
        /// <summary>
        /// Show information, warning and error message.
        /// </summary>
        Info,
        /// <summary>
        /// Show warning and error message.
        /// </summary>
        Warn,
        /// <summary>
        /// Show error message.
        /// </summary>
        Error
    }
}
